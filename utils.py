"""
Przykładowe funkcje matematyczne.
"""


def add(a: int, b: int) -> int:
    """
    Dodaje dwie liczby.

    :param a: Pierwsza liczba.
    :param b: Druga liczba.
    :return: Wynik dodawania.
    """
    return a + b


def subtract(a: int, b: int) -> int:
    """
    Odejmuje jedną liczbę od drugiej.

    :param a: Pierwsza liczba.
    :param b: Druga liczba.
    :return: Wynik odejmowania.
    """
    return a - b


def multiply(a: int, b: int) -> int:
    """
    Mnoży dwie liczby.

    :param a: Pierwsza liczba.
    :param b: Druga liczba.
    :return: Wynik mnożenia.
    """
    return a * b


def divide(a: int, b: int) -> float:
    """
    Dzieli jedną liczbę przez drugą.

    :param a: Liczba dzielona.
    :param b: Liczba dzieląca.
    :return: Wynik dzielenia.
    """
    return a / b


